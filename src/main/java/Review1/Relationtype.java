package Review1;

public class Relationtype {
    public static void main(String[] args) {

        System.out.println(20>19); //  true =for these kind of comparsion the responds boolean (true and False)
        System.out.println(20<19);  // false
        System.out.println(14==15); // false
        System.out.println(14!=15); // the exclamation mark means NOt which is true
        System.out.println(14>=15);  // false not equal not greater
        System.out.println(14<=15);   // true not equal but greater

        // we can also do the above ways in the follwing ways as well.
        boolean  result=20>19;
        System.out.println(result);
        boolean  result1=20<19;
        System.out.println(result1);

    }
}
