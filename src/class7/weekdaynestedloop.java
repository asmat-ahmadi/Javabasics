package class7;

public class weekdaynestedloop {
    public static void main(String[] args) {
        int weeks=2;
        int day=7;
        for (int i = 1; i <=weeks; i++) {
            System.out.println("week:" +i);
            for (int j = 1; j <=day; j++) {
                System.out.println("day: " +j);

            }

        }
    }

}
